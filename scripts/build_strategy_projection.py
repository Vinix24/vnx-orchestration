def seed_decisions(
    output_dir: Path | None = None,
    *,
    dry_run: bool = False,
) -> list[dict[str, str]]:
    """Append 2026-06-01 decisions via ``record_decision()``.

    Idempotent per decision_id: if a decision with the same ID already
    exists in decisions.ndjson, it is skipped. Returns the list of
    actually-appended decision IDs (empty on rerun).
    """
    from strategy.decisions import record_decision

    target_path = _decisions_path(output_dir)
    target_dir = target_path.parent

    # Read existing decision IDs
    existing_ids: set[str] = set()
    if target_path.exists():
        try:
            import json

            for line in target_path.read_text(encoding="utf-8").splitlines():
                if not line.strip():
                    continue
                try:
                    entry = json.loads(line)
                    eid = entry.get("decision_id", "")
                    if eid:
                        existing_ids.add(eid)
                except json.JSONDecodeError:
                    continue
        except OSError:
            pass

    appended: list[dict[str, str]] = []
    for decision in _SEED_DECISIONS:
        if decision["decision_id"] in existing_ids:
            print(
                f"[build_strategy_projection] decision "
                f"{decision['decision_id']} already exists — skipping"
            )
            continue

        if dry_run:
            print(
                f"[dry-run] would record {decision['decision_id']}: "
                f"{decision['scope']}"
            )
            appended.append(decision)
            continue

        target_dir.mkdir(parents=True, exist_ok=True)
        record_decision(
            decision_id=decision["decision_id"],
            scope=decision["scope"],
            rationale=decision["rationale"],
            path=target_path,
        )
        print(
            f"[build_strategy_projection] recorded {decision['decision_id']}"
        )
        appended.append(decision)

    return appended


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------
def main() -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Project root ROADMAP.yaml into strategy/roadmap.yaml + seed "
            "decisions.ndjson"
        )
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Print what would be written; do not write files",
    )
    parser.add_argument(
        "--output-dir",
        default=None,
        help="Output directory (default: .vnx-data/strategy/)",
    )
    parser.add_argument(
        "--skip-decisions",
        action="store_true",
        help="Skip decision seeding (only write roadmap)",
    )
    parser.add_argument(
        "--skip-roadmap",
        action="store_true",
        help="Skip roadmap projection (only seed decisions)",
    )
    args = parser.parse_args()

    output_dir = Path(args.output_dir) if args.output_dir else None
    errors = 0

    if not args.skip_roadmap:
        try:
            projection = build_projection()
            write_strategy_roadmap(projection, output_dir, dry_run=args.dry_run)
        except Exception as exc:
            print(f"[build_strategy_projection] roadmap projection failed: {exc}",
                  file=sys.stderr)
            errors += 1

    if not args.skip_decisions:
        try:
            appended = seed_decisions(output_dir, dry_run=args.dry_run)
            if appended:
                print(
                    f"[build_strategy_projection] seeded {len(appended)} "
                    f"decisions"
                )
            else:
                print(
                    "[build_strategy_projection] no new decisions to seed"
                )
        except Exception as exc:
            print(f"[build_strategy_projection] decision seeding failed: {exc}",
                  file=sys.stderr)
            errors += 1

    return min(errors, 1)


if __name__ == "__main__":
    sys.exit(main())
